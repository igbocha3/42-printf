// Счетчик десятичных

#include "ft_printf.h"

int	ft_len_d(long long d)
{
	int	k;

	k = 1;
	while (d >= 10 || d <= -10)
	{
		k++;
		d /= 10;
	}
	if (d < 0)
		k++;
	return (k);
}
