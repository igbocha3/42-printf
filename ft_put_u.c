// Десятичное число целого типа без знака

#include "ft_printf.h"

int	ft_put_u(unsigned int u)
{
	long int	l;

	l = u;
	if (l >= 10)
		ft_put_u(l / 10);
	ft_put_c(l % 10 + 48);
	return (ft_len_d(u));
}
